public class CountDTO {

	private Long registryUserCount;
	private Long loggedInUserCount;
	private Long registryNagarpalikaCount;
	private Long propertyCount;
	
	public Long getRegistryUserCount() {
		return registryUserCount;
	}
	public void setRegistryUserCount(Long registryUserCount) {
		this.registryUserCount = registryUserCount;
	}
	public Long getLoggedInUserCount() {
		return loggedInUserCount;
	}
	public void setLoggedInUserCount(Long loggedInUserCount) {
		this.loggedInUserCount = loggedInUserCount;
	}
	public Long getRegistryNagarpalikaCount() {
		return registryNagarpalikaCount;
	}
	public void setRegistryNagarpalikaCount(Long registryNagarpalikaCount) {
		this.registryNagarpalikaCount = registryNagarpalikaCount;
	}
	public Long getPropertyCount() {
		return propertyCount;
	}
	public void setPropertyCount(Long propertyCount) {
		this.propertyCount = propertyCount;
	}
	
	
}
